import java.util.*;
public class oddeven {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();

        if (n%2==0) {
            System.out.print("It is an even number");
            
        } else {
            System.out.print("It is an odd number");
            
        }
    }
    
}
